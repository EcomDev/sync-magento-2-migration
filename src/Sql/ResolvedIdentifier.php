declare(strict_types=1);

namespace EcomDev\MagentoMigration\Sql;


class ResolvedIdentifier implements Identifier
{
    /**
     * @var int
     */
    private $value;

    public function __construct(int $value)
    {
        $this->value = $value;
    }

    public function findValue(array $resolved): int
    {
        return $this->value;
    }
}
